// ====================================================================== //
//	Function: GPIO_Init
//		- Initialization GPIO port
// ---------------------------------------------------------------------- //
void GPIO_Init (void)
{
	EALLOW;

	// M_DSP_RUN LED GPIO Setting (GPIO-01)
	GpioCtrlRegs.GPAMUX1.bit.GPIO1 = 0;
	GpioCtrlRegs.GPADIR.bit.GPIO1 = 1;
	GpioDataRegs.GPADAT.bit.GPIO1 = 0;
	
	EDIS;
}
// ====================================================================== //



// ====================================================================== //
//	Function: M_DSP_RUN_LED_Toggle
//		- Toggle M_DSP_RUN LED
// ---------------------------------------------------------------------- //
void M_DSP_RUN_LED_Toggle (void)
{
	GpioDataRegs.GPATOGGLE.bit.GPIO1 = 1;
}
